/** Get mandals for a district */
export function getMandalsForDistrict(districtKey: string): Mandal[] {
    return DISTRICT_MANDALS[districtKey] || []
}

/** Find mandal coordinates by district and mandal key */
export function getMandalCoords(
    districtKey: string,
    mandalKey: string,
): { lat: number; lon: number } | null {
    const mandals = DISTRICT_MANDALS[districtKey]
    if (!mandals) return null
    const mandal = mandals.find((m) => m.key === mandalKey)
    return mandal ? { lat: mandal.lat, lon: mandal.lon } : null
}

/** Get total count of mandals across all districts */
export function getTotalMandalCount(): number {
    return Object.values(DISTRICT_MANDALS).reduce(
        (sum, mandals) => sum + mandals.length,
        0,
    )
}

/** Search mandals by name (English or Telugu) across all districts */
export function searchMandals(
    query: string,
): (Mandal & { district: string })[] {
    const lowerQuery = query.toLowerCase()
    const results: (Mandal & { district: string })[] = []
    for (const [district, mandals] of Object.entries(DISTRICT_MANDALS)) {
        for (const mandal of mandals) {
            if (
                mandal.en.toLowerCase().includes(lowerQuery) ||
                mandal.te.includes(query)
            ) {
                results.push({ ...mandal, district })
            }
        }
    }
    return results
}
